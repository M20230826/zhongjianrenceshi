#ifndef __SHOW_H
#define __SHOW_H

#include "sys.h"

// 函数声明

/**************************************************************************
函数功能：显示OLED内容
输入参数：无
返回  值：无
**************************************************************************/
void oled_show(void);

/**************************************************************************
函数功能：应用程序显示函数，用于显示特定应用程序的界面
输入参数：无
返回  值：无
**************************************************************************/
void APP_Show(void);

/**************************************************************************
函数功能：数据范围显示函数，可能用于显示数据的范围或图表
输入参数：无
返回  值：无
**************************************************************************/
void DataScope(void);

/**************************************************************************
函数功能：在OLED上绘制一个垂直的点
输入参数：x - 点的横坐标（0~127）
          y - 点的纵坐标（0~63）
          t - 1表示点亮，0表示熄灭
返回  值：无
**************************************************************************/
void OLED_DrawPoint_Shu(u8 x, u8 y, u8 t);

/**************************************************************************
函数功能：一次性显示OLED内容，可能用于初始化或刷新显示
输入参数：无
返回  值：无
**************************************************************************/
void oled_show_once(void);

#endif // __SHOW_H
